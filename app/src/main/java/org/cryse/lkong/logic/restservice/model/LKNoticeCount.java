package org.cryse.lkong.logic.restservice.model;

public class LKNoticeCount {
    private int notice;
    private int atme;
    private int rate;
    private int fans;
    private int pm;

    public int getNotice() {
        return notice;
    }

    public void setNotice(int notice) {
        this.notice = notice;
    }

    public int getAtme() {
        return atme;
    }

    public void setAtme(int atme) {
        this.atme = atme;
    }

    public int getRate() {
        return rate;
    }

    public void setRate(int rate) {
        this.rate = rate;
    }

    public int getFans() {
        return fans;
    }

    public void setFans(int fans) {
        this.fans = fans;
    }

    public int getPm() {
        return pm;
    }

    public void setPm(int pm) {
        this.pm = pm;
    }
}
